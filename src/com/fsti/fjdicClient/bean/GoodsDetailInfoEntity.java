package com.fsti.fjdicClient.bean;

import java.io.Serializable;

/**
 *商品详情信息实体
 * @author 王久叶 
 *
 */
public class GoodsDetailInfoEntity implements Serializable {
	/**
	 *商品ID
	 */
	private String ID;
	/**
	 *商家ID
	 */
	private String shopsID;
	/**
	 *商品类型 1:社区商城  2:团购
	 */
	private int type;
	/**
	 * 商品名称
	 */
	private String name;
	/**
	 * 商品简介
	 */
	private String intro;
	/**
	 * 商品价格
	 */
	private String price;
	/**
	 *最近的销售数量
	 */
	private int selledCount;
	/**
	 *运费类型   有多种运费类型用“※”分隔，如“快递※EMS”
	 */
	private String freightType;
	/**
	 * 运费价格  与运费类型配对，有多种运费价格用“※”分隔,如“15.00※25.00”
	 */
	private String freightPrice;
	/**
	 * 商品图片地址   如有多张图片用“※”分隔
	 */
	private String imageUrl;
	/**
	 *收藏人气
	 */
	private int popularity;
	/**
	 *商品类型名称
	 */
	private String typeName;
	/**
	 *库存量
	 */
	private int storageCount;
	/**
	 *商品到期时间，团购类型的商品使用，格式为“yyyy-MM-dd HH:mm:ss”
	 */
	private String expireDate;
	/**
	 *购物车中选商品，是否选中  true:选中  false:未选中
	 */
	private String NowDate;
	/**	
	 *商品分类ID
	 */
	private String FID;
	/**	
	 *商品原价
	 */
	private String originalCost ;
	public String getOriginalCost() {
		return originalCost;
	}
	public void setOriginalCost(String originalCost) {
		this.originalCost = originalCost;
	}
	public String getFID() {
		return FID;
	}
	public void setFID(String fID) {
		FID = fID;
	}
	
	public String getNowDate() {
		return NowDate;
	}
	public void setNowDate(String nowDate) {
		NowDate = nowDate;
	}
	private boolean isSelect = false;
	
	public boolean isSelect() {
		return isSelect;
	}
	public void setSelect(boolean isSelect) {
		this.isSelect = isSelect;
	}
	
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getShopsID() {
		return shopsID;
	}
	public void setShopsID(String shopsID) {
		this.shopsID = shopsID;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public int getSelledCount() {
		return selledCount;
	}
	public void setSelledCount(int selledCount) {
		this.selledCount = selledCount;
	}
	public String getFreightType() {
		return freightType;
	}
	public void setFreightType(String freightType) {
		this.freightType = freightType;
	}
	public String getFreightPrice() {
		return freightPrice;
	}
	public void setFreightPrice(String freightPrice) {
		this.freightPrice = freightPrice;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public int getPopularity() {
		return popularity;
	}
	public void setPopularity(int popularity) {
		this.popularity = popularity;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	public int getStorageCount() {
		return storageCount;
	}
	public void setStorageCount(int storageCount) {
		this.storageCount = storageCount;
	}
	public String getExpireDate() {
		return expireDate;
	}
	public void setExpireDate(String expireDate) {
		this.expireDate = expireDate;
	}


}
