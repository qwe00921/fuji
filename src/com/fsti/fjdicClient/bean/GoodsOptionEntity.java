package com.fsti.fjdicClient.bean;

import android.widget.ImageView;
import android.widget.RelativeLayout;

/**
 * GoodsList选项信息  人气、新品、价格、销量
 * @author 王久叶 
 *
 */
public class GoodsOptionEntity {
	//排序状态，true时为降序，false时为升序
	private boolean orderState;
	//RelativeLayout即选项的名字
	private RelativeLayout rlName;
	//ImageView即排序图标
	private ImageView ivOrder;
	public GoodsOptionEntity(boolean orderState,RelativeLayout rlName,ImageView ivOrder){
		this.orderState = orderState;
		this.rlName = rlName;
		this.ivOrder = ivOrder;
	}

	public boolean isOrderState() {
		return orderState;
	}

	public void setOrderState(boolean orderState) {
		this.orderState = orderState;
	}

	public RelativeLayout getRlName() {
		return rlName;
	}
	public void setRlName(RelativeLayout rlName) {
		this.rlName = rlName;
	}
	public ImageView getIvOrder() {
		return ivOrder;
	}
	public void setIvOrder(ImageView ivOrder) {
		this.ivOrder = ivOrder;
	}
	
	
	
}
